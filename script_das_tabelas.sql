-- Criação da tabela TBL_TIPO_FONTES
CREATE TABLE TBL_TIPO_FONTES (
    ID_TIPO_FONTE NUMBER(*, 0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(50) NOT NULL
);

-- Criação da tabela TBL_REGIOES_SUSTENTAVEIS
CREATE TABLE TBL_REGIOES_SUSTENTAVEIS (
    ID_REGIAO NUMBER(*, 0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(50) NOT NULL
);

-- Criação da tabela TBL_PROJETOS_SUSTENTAVEIS
CREATE TABLE TBL_PROJETOS_SUSTENTAVEIS (
    ID_PROJETO NUMBER(*, 0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    DESCRICAO VARCHAR2(255) NOT NULL,
    CUSTO NUMBER(10,2) NOT NULL,
    STATUS VARCHAR2(50) NOT NULL,
    ID_TIPO_FONTE NUMBER(*, 0) NOT NULL,
    ID_REGIAO NUMBER(*, 0) NOT NULL,
    CONSTRAINT FK_PROJETO_TIPO_FONTE FOREIGN KEY (ID_TIPO_FONTE) REFERENCES TBL_TIPO_FONTES(ID_TIPO_FONTE),
    CONSTRAINT FK_PROJETO_REGIAO FOREIGN KEY (ID_REGIAO) REFERENCES TBL_REGIOES_SUSTENTAVEIS(ID_REGIAO)
);

-- Criação da tabela TBL_EMISSOES_CARBONO
CREATE TABLE TBL_EMISSOES_CARBONO (
    ID_EMISSAO NUMBER(*, 0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ID_TIPO_FONTE NUMBER(*, 0) NOT NULL,
    EMISSAO NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_EMISSAO_TIPO_FONTE FOREIGN KEY (ID_TIPO_FONTE) REFERENCES TBL_TIPO_FONTES(ID_TIPO_FONTE)
);